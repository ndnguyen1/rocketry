class ActuatorModel:
    @staticmethod
    def clip(value, min_val, max_val):
        return max(min_val, min(value, max_val))

    @staticmethod
    def update(u, x, dx, natural_frequency, damping_ratio,
               max_deflection, min_deflection, rate_limit, dt):
        wn = natural_frequency
        zeta = damping_ratio

        # Compute the acceleration based on the control input and current state
        ddx = (u - x) * (wn ** 2) - 2.0 * zeta * wn * dx

        # Update velocity with rate limiting (Euler integration)
        dx += ddx * dt
        dx = ActuatorModel.clip(dx, -rate_limit, rate_limit)

        # Update position with saturation (Euler integration)
        x += dx * dt
        x = ActuatorModel.clip(x, min_deflection, max_deflection)

        return x, dx  # Return updated position and velocity

